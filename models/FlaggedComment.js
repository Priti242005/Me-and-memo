const mongoose = require('mongoose');

const { Schema } = mongoose;

/**
 * Stores toxic comment attempts for optional admin review (not shown publicly).
 */
const flaggedCommentSchema = new Schema(
  {
    postId: {
      type: Schema.Types.ObjectId,
      ref: 'Post',
      required: true,
      index: true,
    },
    userId: {
      type: Schema.Types.ObjectId,
      ref: 'User',
      required: true,
      index: true,
    },
    text: {
      type: String,
      required: true,
      trim: true,
      maxlength: 500,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  },
  { versionKey: false }
);

module.exports = mongoose.model('FlaggedComment', flaggedCommentSchema);
